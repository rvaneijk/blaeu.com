          <h2 class="text-2xl font-bold text-gray-900 mt-8 mb-4">Five Themes Shaping the Debate</h2>

          <p class="mb-4">
            The panel was structured around five interconnected themes that together paint a picture
            of a regulatory landscape under pressure from rapid technological change.
          </p>

          <h3 class="text-xl font-semibold text-gray-900 mt-6 mb-3">
            1. Problem Definition: What Does the Digital Omnibus Aim to Address?
          </h3>

          <p class="mb-4">
            The opening discussion examined the scope and ambition of the Digital Omnibus proposal.
            Panelists explored what problems the proposal genuinely aims to solve versus the risk of
            creating new regulatory gaps. The conversation revealed differing perspectives on
            whether consolidation of digital regulation strengthens or dilutes existing protections.
          </p>

          <h3 class="text-xl font-semibold text-gray-900 mt-6 mb-3">
            2. Legal Architecture: Shifts Affecting the Consent-First Principle
          </h3>

          <p class="mb-4">
            A central concern was the potential impact on Article 5(3) of the ePrivacy Directive,
            which establishes the consent-first principle for accessing information stored on
            terminal equipment. The panel examined how proposed changes to the legal architecture
            could shift the balance between user autonomy and business interests, and what
            safeguards would be needed to preserve meaningful consent.
          </p>

          <h3 class="text-xl font-semibold text-gray-900 mt-6 mb-3">
            3. Contested Analytics exemptions
          </h3>

          <p class="mb-4">
            The discussion turned to one of the most contentious areas: carved-out exemptions for
            analytics. While some argue that audience measurement serves legitimate interests and
            should not require explicit consent, others warn that broadly defined analytics
            exemptions could become loopholes that undermine the consent framework entirely. The
            panelists debated where to draw the line.
          </p>

          <h3 class="text-xl font-semibold text-gray-900 mt-6 mb-3">
            4. Practical Consent Mechanisms
          </h3>

          <p class="mb-4">
            Moving from legal theory to practice, the panel addressed the state of consent
            mechanisms on the ground. Machine-readable signals offer a promising path toward
            scalable consent management, but adoption remains uneven. Meanwhile, dark patterns
            continue to undermine informed choice, and consent fatigue means users increasingly
            click through prompts without meaningful engagement. The gap between regulatory intent
            and user experience remains wide.
          </p>

          <h3 class="text-xl font-semibold text-gray-900 mt-6 mb-3">
            5. Future-Proofing: AI Agents and the Definition of "User"
          </h3>

          <p class="mb-4">
            Perhaps the most forward-looking theme was the challenge posed by AI agents. As AI
            systems increasingly browse the web and interact with consent mechanisms on behalf of
            humans, the panel confronted a fundamental question: when an AI agent consents on our
            behalf, who exactly is the "user" that needs terminal equipment protection under
            ePrivacy frameworks designed around human operators? This question has no easy answer,
            but should get urgent attention from regulators and the legislator.
          </p>

          <h2 class="text-2xl font-bold text-gray-900 mt-8 mb-4">
            Privacy-Enhancing Technologies in Digital Advertising
          </h2>

          <p class="mb-4">
            In a related session during the same week, the Lisbon Council hosted a High-Level
            Working Lunch on "Privacy-Enhancing Technologies in Digital Advertising." The
            invitation-only gathering brought together representatives from FEDMA, Microsoft, the
            European Commission, The European Consumer Organisation, Zalando, Mozilla, IAB Europe,
            the Future of Privacy Forum, and the European Data Protection Board.
          </p>

          <p class="mb-4">
            The discussion explored whether privacy-enhancing technologies (PETs) represent a way
            around privacy rules or an important way forward for Europe's competitiveness.
            Participants examined the bottlenecks hindering the PETs ecosystem, the incentives
            needed to drive adoption while safeguarding personal data, and the real-world impact of
            PETs deployment.
          </p>

          <p class="mb-4">
            With Europe's shift toward new data protection provisions in the Digital Omnibus, PETs
            are increasingly positioned not just as compliance tools but as strategic innovation
            enablers &mdash; one of the fastest-growing markets in data protection.
          </p>

          <h2 class="text-2xl font-bold text-gray-900 mt-8 mb-4">Looking Ahead</h2>

          <p class="mb-4">
            The discussions at Data Protection Day 2026 and the Lisbon Council working lunch
            underscore a common theme: Europe's regulatory framework is at a crossroads. The choices
            made in the Digital Omnibus process will determine whether the consent-first principle
            remains the cornerstone of digital privacy in Europe, or whether it gets diluted by
            exemptions and new technological realities that the original frameworks never
            anticipated.
          </p>

