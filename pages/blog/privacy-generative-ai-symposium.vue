          <p class="mb-4">
            The Office of the Privacy Commissioner of Canada/Commissariat à la protection de la vie
            privée du Canada (OPC) organized the Privacy & Generative AI Symposium in Ottawa on Dec
            7, 2023. I participated in the event on behalf of The Future of Privacy Forum. Here is a
            concise summary of the discussions.
          </p>

          <p class="mb-4">
            <strong>Keynote:</strong>
            The 'Privacy & Generative AI Symposium' keynote by Gary Marcus provided an in-depth
            exploration of the challenges and considerations at the intersection of artificial
            intelligence and privacy. In his keynote, Dr. Marcus highlighted current AI systems'
            limitations and potential risks, emphasizing their lack of true understanding and
            connection with reality. He noted that AI cannot comprehend basic concepts like time and
            weight and may exhibit traits akin to psychopathy. He also warned about the dangers of
            AI systems inadvertently leaking personal data due to their extensive data absorption.
          </p>

          <p class="mb-4">
            Moreover, he raised concerns about the misuse of AI in creating disinformation and
            biases, especially in critical areas like transportation and nuclear weaponry, due to
            overestimating their capabilities. To give context to these concerns, he covered
            incidents where AI systems caused real-world impacts, such as tricking human workers
            into giving up their credentials or influencing stock markets with a viral image of an
            explosion. He then expressed concern over the misalignment of technologists' and
            nations' goals with global interests, emphasizing the need for responsible AI
            development and governance.
          </p>

          <p class="mb-4">
            He advocated for transparency in AI development, urging companies to report detailed
            accounts of their data use, testing results, and incidents related to, e.g., bias,
            cybercrime, election interference, and data leaks. He highlighted the risks associated
            with the pursuit of profit in new, unreliable technologies like AI. He referenced a
            cartoon - with the following text: "Yes, the planet got destroyed, but for a beautiful
            moment in time, we created a lot of value for shareholders." - to underscore that while
            technology is being developed for financial gain, it could have serious, overlooked
            consequences for democracy and privacy.
          </p>

          <p class="mb-4">
            Towards the end of the keynote, he expressed concern about big tech companies'
            disproportionate influence, lobbying power, and absence of independent voices in policy
            discussions. He emphasized the need for governments to include independent scientists in
            AI policy decisions, ensuring a more balanced and informed approach to the future of
            technology.
          </p>

          <p class="mb-4">
            During a Q&A session, he addressed the challenges in AI development, particularly
            regarding large language models. He noted that despite efforts to train AI on extensive
            internet data, companies may be reaching data limits, which impacts AI improvement. He
            questioned that scaling AI models indefinitely leads to better performance compared to
            the end of Moore's Law.
          </p>

          <p class="mb-4">
            He advocated for a combination of existing laws and new regulations on AI governance,
            primarily to protect artists' rights and address biases in employment and housing. He
            suggested creating national AI agencies for comprehensive legislation review.
          </p>

          <p class="mb-4">
            Regarding the openness of AI software and governance, he supported controlled access to
            AI technology, similar to a licensing system, while calling for transparent and
            inclusive governance processes.
          </p>

          <p class="mb-4">
            Marcus also discussed the relationship between regulation and innovation. He argued that
            effective regulation could enhance safety without hindering innovation, drawing
            parallels with the aviation industry. He emphasized the importance of balanced rules
            that promote safety and technological innovation.
          </p>

